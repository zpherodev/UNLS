<h1>Understanding Subnodes</h1>
<p>
  Subnodes are the extended details of the main nodes. They provide deeper meaning and structure
  in the Universal Node Language System (UNLS).
</p>

<h2>Key Characteristics of Subnodes</h2>

<h3>1. Further Details</h3>
<p>Subnodes refine the main nodes by offering:</p>
<ul>
  <li>Specific details</li>
  <li>Niche categorization</li>
  <li>Differentiation from broader categories</li>
  <li>Deeper root meanings</li>
</ul>

<h3>2. Mutually Exclusive</h3>
<p>
  Subnodes are triggered exclusively by inheritance. When a word is parsed into a specific subnode,
  it typically cannot belong to another. This exclusivity:
</p>
<ul>
  <li>Saves parsing time by narrowing down options</li>
  <li>Clarifies word meaning through elimination</li>
  <li>Prevents overlap or ambiguity within a node’s child branches</li>
</ul>
<p><strong>Example: <code>Today</code></strong></p>
<blockquote>
  Triggers <strong>Spatial</strong>. Possible subnodes are <em>Time</em> and <em>Space</em>.<br>
  We recognize it refers to time. It’s not past or future—thus, it resolves to: <strong>Present/Now</strong>.
</blockquote>

<h3>3. Optional Use</h3>
<p>
  Not every word requires subnode refinement. Some broadly applicable words function sufficiently
  at the main node level.
</p>
<p><strong>Example: <code>The</code></strong></p>
<blockquote>
  “The” does not provide new information on its own. It simply emphasizes the next word’s focus.
  Thus, it stays in the main node: <strong>Focus</strong>.
  <br><br>
  Example stack: <code>The (Focus) → ______ (Action, Identity, Focus, Quantitative...)</code>
</blockquote>

<h3>4. Give Value Beyond Numbers</h3>
<p>
  While the <strong>Quantitative</strong> node gives numerical or measurable values, subnodes
  often deal with <em>subjective</em> and <em>abstract</em> value judgments—especially adjectives.
</p>
<p><strong>Example: <code>Beautiful</code></strong></p>
<blockquote>
  The word “Beautiful” provides a positive, subjective valuation.<br>
  To confirm, examine its polarity: <code>Ugly</code> (a negative value).<br>
  Therefore, “Beautiful” maps to: <strong>Focus → Subjective → Abstract → Positive</strong>.
</blockquote>


<h1>Context Matrices</h1>
<p>
  The context matrices provide a framework for how nodes and subnodes interact when stacked next to each other.
  They are used to establish context within a sentence.
</p>

<p>
  By taking any two nodes or subnodes, we can locate their convergence point in the context matrix and determine how they
  combine to form contextual meaning.
</p>

<p>
  The first node or subnode selects the <strong>column</strong>, and the second selects the <strong>row</strong>. The
  intersecting cell provides the resulting context.
</p>

<h3>Example</h3>
<ul>
  <li><strong>First Node:</strong> Focus</li>
  <li><strong>Second Node:</strong> Action → Current</li>
  <li><strong>Matrix Cell Result:</strong> A focus on an action that is currently occurring.</li>
</ul>
