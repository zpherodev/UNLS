<h1>System Instructions</h1>
<p>
  These instructions are minimal, simple, and universally applicable across all languages.
</p>

<h2>Step 1: Trigger Node(s)</h2>
<p>
  Each word in a sentence triggers a <strong>main node</strong>. In rare cases, a word may trigger more than one main node,
  but <strong>never more than one subnode per main node</strong>. Once a node is selected, proceed to the next word.
</p>

<h2>Step 2: Stack the Nodes</h2>
<p>
  Nodes are <strong>stacked in the order they appear</strong> in the sentence. This sequence builds both contextual meaning
  and the root structure of the sentence.
</p>

<h2>Step 3: Analyze the Node Stack</h2>
<p>
  Use the <strong>context matrix</strong> to analyze the relationships between adjacent nodes. Each pair can be matched
  to a matrix cell to determine how the meanings interact.
</p>

<h2>Step 4: Determine Root Meaning</h2>
<p>
  After applying the context matrix, the root meaning of the sentence should become both <strong>clear</strong> and
  <strong>contextual</strong>. The resulting node stack is:
</p>
<ul>
  <li><strong>Universal</strong> across all languages</li>
  <li>Usable for <strong>reverse translation</strong></li>
  <li>Dependent on a <strong>node dictionary</strong> for the target language</li>
</ul>
<p>
  By matching definitions through the main and subnode structure, accurate translation and cross-language understanding
  becomes possible.
</p>

<h1>Instructions for Translations</h1>
<p>
  Use this step-by-step process to translate any sentence or word set using the Universal Node Language System.
</p>

<h2>Step 1: Create the Node Stack</h2>
<p>
  Begin by parsing the sentence or words being translated. Assign each word to its appropriate <strong>node</strong> and <strong>subnode</strong>, and stack them in the order they appear.
</p>

<h2>Step 2: Analyze the Stack</h2>
<p>
  Use the <strong>context matrix</strong> to understand how the nodes relate to each other within the stack.
</p>

<h2>Step 3: Observe Root Meaning</h2>
<p>
  Determine the <strong>core meaning</strong> of the sentence based on the structure and relationships found in the node stack.
</p>

<h2>Step 4: Match Nodes to Target Language</h2>
<p>
  Identify words in the <strong>target language</strong> that trigger the same nodes and subnodes, and produce equivalent context.
</p>

<h2>Step 5: Use a Translation Dictionary</h2>
<p>
  Utilize a translation dictionary to <strong>narrow down the list</strong> of possible words in the target language and match them to the intended meaning.
</p>

<h2>Step 6: Replace Nodes with Words</h2>
<p>
  Insert the selected target-language words in the same order as the original node stack. This ensures both structural and contextual fidelity in the translated sentence.
</p>


<h1>Translating Incomplete or Unfinished Sentences</h1>
<p>
  When dealing with incomplete sentences, the <strong>node stack</strong> can reveal what’s missing and
  still help derive the core meaning. The <strong>context matrix</strong> provides additional clarity,
  even when parts of the sentence are absent.
</p>

<h2>Example:</h2>
<blockquote>
  <strong>Sentence Fragment:</strong> Cat — cradle with silver —<br>
</blockquote>

<h3>Node Stack:</h3>
<ul>
  <li><strong>Identity → Species:</strong> Cat</li>
  <li><strong>Focus → Objective → Material Object:</strong> Cradle</li>
  <li><strong>Spatial → Space → Close/Near:</strong> (implied proximity)</li>
  <li><strong>Focus → Subjective → Abstract → Creative:</strong> Silver (as a creative descriptor)</li>
</ul>

<h3>Analysis:</h3>
<p>
  Although incomplete, the sentence can still be interpreted through its node structure:
</p>
<ul>
  <li>“Cat” is identified as a species.</li>
  <li>There is a focus on a material object ("cradle") nearby.</li>
  <li>“Silver” acts as a creative descriptor, likely modifying the material object.</li>
</ul>

<p>
  From these nodes, we infer a spatial relationship and a missing object of identity—something goes into a cradle,
  and “silver” typically modifies a known set of physical objects.
</p>

<h3>Likely Completion:</h3>
<blockquote>
  <strong>Cat in cradle with silver spoon.</strong>
</blockquote>
<p>
  Even in cases where the sentence is unfinished, the node framework offers enough structure to fill in gaps
  logically and meaningfully.
</p>
